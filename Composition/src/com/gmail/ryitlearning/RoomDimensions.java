package com.gmail.ryitlearning;

public class RoomDimensions {
    private int height;
    private int width;
    private int length;

    public RoomDimensions(int height, int width, int length) {
        this.height = height;
        this.width = width;
        this.length = length;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }

    public int getLength() {
        return length;
    }
}
